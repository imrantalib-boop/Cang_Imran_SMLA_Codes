%% Chebyshev function vector
function Psi=funvec(N,L)
syms k  t 
for i=0:N
a1=(-1)^(i-k);
a2=gamma(i+k);
a3=2^(2*k);
d1=gamma(i-k+1);
d2=gamma(2*k+1);
d3=L^k;
b=i;
aa=(a1*a2*a3*b)/(d1*d2*d3);
Psi(i+1)=symsum(aa*t^k,k,0,i);
Psi(1,1)=1;
end
Psi=transpose(Psi)
end
